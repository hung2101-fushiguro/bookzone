package model;

/**
 *
 * @author ADMIN
 */
public class Accessory {
    private int id;
    private String name;
    private String description;
    private double price;
    private int quantity;
    private String imageUrl;
    private AccessoryCategory category;
    private String createdAt;

    public Accessory() {
    }

    public Accessory(int id, String name, String description, double price, int quantity, String imageUrl, AccessoryCategory category, String createdAt) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
        this.quantity = quantity;
        this.imageUrl = imageUrl;
        this.category = category;
        this.createdAt = createdAt;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public AccessoryCategory getCategory() {
        return category;
    }

    public void setCategory(AccessoryCategory category) {
        this.category = category;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    @Override
    public String toString() {
        return "Accessory{" + "id=" + id + ", name=" + name + ", description=" + description + ", price=" + price + ", quantity=" + quantity + ", imageUrl=" + imageUrl + ", category=" + category + ", createdAt=" + createdAt + '}';
    }
    
    
}